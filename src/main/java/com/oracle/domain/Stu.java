package com.oracle.domain;

public class Stu {
	private Integer sid; //学生编号 
	private String sname; //学生姓名
	public Integer getSid() { 
		return sid; 
	}
	public void setSid(Integer sid) { 
		this.sid = sid; 
	}
	public String getSname() { 
		return sname; 
	}
	public void setSname(String sname) { 
		this.sname = sname; 
	}
}
